print("더하기: ", 10+20)
print("빼기: ", 20-10)
print("곱하기: ", 10*10)
print("나누기: ", int(20/2))

# 거듭제곱의 연산도 가능
print(" c**2: ", 10**2)
print(" c**2: ", 10**3)
print(" c**2: ", 10**4)

# 목(//)과 나머지(%) 구하는 연산
print("몫: ", 40//6)
print("나머지: ", 40%6)

# 논리 연산 (or, and, not)
print(0 or 1)
print(1 or 0)
print(0 or 1)
print(1 or 1)